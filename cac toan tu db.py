# ham and &
a = 6  # 6 trong hệ nhị phân là 110
b = 3  # 3 trong hệ nhị phân là 011
result = a & b  # AND : 110 & 011 = 010 (kết quả là 2 trong hệ thập phân)
print(result)  # Kết quả là 2

# ham or |
a = 5  # 5 trong hệ nhị phân là 101
b = 3  # 3 trong hệ nhị phân là 011
result = a | b  # OR : 101 | 011 = 111 (kết quả là 7 trong hệ thập phân)
print(result)  # Kết quả là 7

# ham xor ^
a = 5  # 5 trong hệ nhị phân là 101
b = 3  # 3 trong hệ nhị phân là 011
result = a ^ b  # XOR : 101 ^ 011 = 110 (kết quả là 6 trong hệ thập phân)
print(result)  # Kết quả là 6

# ham dich trai << va dich phai >>
a = 5  # 5 trong hệ nhị phân là 00000101
result = a << 2  # Dịch sang trái 2 bit: 00010100 (kết quả là 20)
print(result)  # Kết quả là 20

a = 20  # 20 trong hệ nhị phân là 00010100
result = a >> 2  # Dịch sang phải 2 bit: 00000101 (kết quả là 5)
print(result)  # Kết quả là 5





